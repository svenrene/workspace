package wbs.operators_decisions;

public class ZuweisungDemo {

	// der zuweisungsoperator ist rechtsassoziativ
	
	public static void main(String[] args) {

		int n1 = 2;
		int n2 = 3;
		int n3 = 4;
		
		n1 = n2 = n3;
		
		System.out.println(n1);
		System.out.println(n2);
		
		
	}

}
