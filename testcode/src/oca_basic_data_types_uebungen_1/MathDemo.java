package oca_basic_data_types_uebungen_1;

public class MathDemo {
	public static void main(String[] args) {
		System.out.println(Math.round(-1.5)); // -1
		System.out.println(Math.round(-0.5)); // 0
		System.out.println(Math.round(0.5)); // 1
		System.out.println(Math.round(1.5)); // 2
		System.out.println(Math.round(Math.sqrt(-1.0))); // -1.0
		System.out.println((int) Math.floor(1.9)); // ?
		System.out.println((int) Math.ceil(1.9)); // ?
	}
}
